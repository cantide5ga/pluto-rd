function entryDriver(keyword, offset, count) {
    var entries = [];   
    var lCased = keyword ? keyword.toLowerCase() : null;
        
    switch(lCased) {
        case element.CALCIUM: entries.push(pentagonite, fluorite, epidote); break;
        case element.VANADIUM: entries.push(pentagonite); break;
        case element.SILICON: entries.push(pentagonite, chromdravite, chrysocolla, agate, epidote); break;
        case element.HYDROGEN: entries.push(pentagonite, chromdravite, chrysocolla, epidote); break;
        case element.OXYGEN: entries.push(pentagonite, chromdravite, chrysocolla, agate, epidote); break;
        case element.FLUORINE: entries.push(fluorite); break;
        case element.SODIUM: entries.push(chromdravite); break;
        case element.MAGNESIUM: entries.push(chromdravite); break;
        case element.IRON: entries.push(chromdravite, epidote); break;
        case element.BORON: entries.push(chromdravite); break;
        case element.CHROMIUM: entries.push(chromdravite); break;
        case element.ALUMINUM: entries.push(chrysocolla, epidote); break;
        case element.COPPER: entries.push(chrysocolla); break;
        default: entries.push(fluorite, pentagonite, chromdravite, chrysocolla);
    }
    
    return { pagedEntries: entries.slice(offset, offset + count), totalCount: entries.length };
}
		
